                <p>Drama is very much geared towards those with a fondness for self-expression. Those who like to thrill, shock and have the confidence to unhesitatingly speak their minds. And Drama, befitting its name, can be just as creative and unpredictable as they are.</p>
                <p>Those people are especially likely to admire the scenery of the club's gold room, the wall comprising of Japanese cat figurines, and wallpaper – sourced from New York – with a distinct scent of banana. Eccentric doesn’t even begin to describe it.</p>
                <p>And that’s before we even mention the pink poodles and peculiar champagne rituals that stand to provoke and humour you, should you spend time at this bold and bouncy club.</p>
                <p>There’s certainly a strong artistic flavour to Drama’s array of sections, in which you can dance away to an eclectic range of music emanating from the building’s fully immersive Funktion-One sound system.</p>
                <p>If all of this sounds like it’s for you, consider that Drama is located at the luxurious Hilton hotel on Park Lane, and open Thursdays to Sundays, from 11pm to 3am.</p>

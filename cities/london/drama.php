              <div class="description">
                <p>There was quite a stir when, in September 2015, the nightclub Drama opened on Hertford Street in Mayfair. And we shouldn’t be surprised that there was such a commotion, considering the credentials of the people behind the joint...</p>
                <p>Drama was formed by the entrepreneurs Nick House, Ryan Bish and Tom Berg. Between them, they have the successful nightclubs Mahiki, Whisky Mist and Cirque Le Soir on their CVs – so they clearly know what the most elite of the capital's revellers like.</p>

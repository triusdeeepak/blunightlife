              <h2>Overview</h2>

              <div class="description">
                <p>Befitting its name, which comes from the colonial mogul Sir Thomas Stamford Raffles, the nightclub Raffles owes much of its current renown to its history. Since opening in 1967, the venue has become a magnet of much of London’s political and creative elite.</p>
                <p>Raffles was originally set up by the restauranteur Peter Evans in Kings Road, which has strong associations with the Rolling Stones and Eric Clapton. Public figures that have visited the venue itself include Princess Margaret and Barbara Streisand.</p>
                <p>Even the Queen has attended Raffles – specifically, with Prince Charles and Princess Anne in the early 1970s to celebrate the 21st birthday of the grandson of the Earl of Mountbatten.</p>
                <p>The Chelsea-based club’s great reputation has remained to the current day. Though previously considered a stamping ground for well-heeled locals, Raffles has long offered a range of services for London night revellers – including drinks, food and dancing.</p>
                <p>Raffles is a private members club, and the lucky members appreciate the venue’s feeling of exclusivity. The core attraction, however, remains the music – and, with many of the best music artists and DJs regularly visiting Raffles, it shouldn’t be that big a surprise.</p>
                <p>In fact, today, the name of Raffles refers not just to a club, but to a powerful brand. This has been encouraged by the launch of the Raffles Lifestyle Membership scheme, the perks of which include intimate performances from top musicians and appealing rates with Raffle affiliate clubs overseas.</p>
                <p>Want to join the fun at Raffles? You have to be a member – or, at least, a guest of an existing member. You also have to adhere to the “smart casual” dress code – which includes dark jeans and forbids shorts, polo shirts, vests, sports trainers and baseball caps.</p>
                <p>If you do meet all of the vital criteria, however, you can embrace Raffles during the venue’s opening hours of 10.30pm until late from Tuesday to Saturday. Time to see what all the fuss is about...</p>

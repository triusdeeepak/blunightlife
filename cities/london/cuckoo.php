              <h2>Overview</h2>

              <div class="description">
                <p>It's hard not to be struck by The Cuckoo Club upon first walking through its doors. It's certainly a visual spectacle; but, more than that, it's one of London’s must-visit night spots.</p>
                <p>The nightclub's design owes much to Barbara Hulanicki, best-known as the founder of the renowned fashion house Biba, and brings to mind Slash meets Alice in Wonderland. The cool and quirky décor lends a distinct feeling of luxury at every turn.</p>
                <p>In fact, the venue has been specifically designed for late night London party-goers disillusioned with seeing a succession of identikit, soulless bars. There's certainly nothing soulless about the founding committee behind The Cuckoo Club, which is packed with much of the capital's leading creative talent.</p>
                <p>But how quirky is the venue itself? Well, there's mahogany, velvet, a hot pink dance floor, and mirrors which better allow you to enjoy the thrill of seeing it all. There's a highly ostentatious, rock-chic character to the place, which spans an impressive 5000 square feet.</p>
                <p>There are two floors of this eccentricity to check out. And that’s assuming that you aren't even distracted by the opportunities to get comfortable with star revellers, intimate with your clubbing and refined with your cocktail drinking.</p>
                <p>No two visits to The Cuckoo Club should ever feel the same. That's thanks largely to the eclectic programme of music and events, including performances from some of the planet's top DJs. The venue’s flexibility means that a range of both small and large events – there’s a capacity of up to 400 guests – can be held there.</p>
                <p>The different spaces include a ground floor cocktail bar, the Gold Room – great for live music – and a DJ box complete with Funktion One sound system. You can enjoy all of this when you head down to the building of The Cuckoo Club on London’s Swallow Street.</p>
